package com.Kamesh.projects.PractisingJavaDataTypes;

public class ORVAndPDT2 {
	float f = 110.11f;
	double d = 120.12;
	char c = 'A';
	boolean b = true;

	public static void main(String[] args) {
		byte b = 70;
		short s = 80;
		long l = 90;
		int i = 100;
		System.out.println("Byte value:" + b);
		System.out.println("Short value:" + s);
		System.out.println("Long value:" + l);
		System.out.println("integer value:" + i);
		ORVAndPDT2 o2 = new ORVAndPDT2();
		System.out.println("Float value:" + o2.f);
		System.out.println("Double value:" + o2.d);
		System.out.println("Character value:" + o2.c);
		System.out.println("Boolean value:" + o2.b);
	}
}
