package com.Kamesh.projects.JavaDataTypes;

public class DeclaringAndInitialiseVariable
{
	public static void main(String[] args)
	{
		String empname;       //Declaration of variable 
		empname="Kamesh";     //Initialization of variable
		
		int empid=1212;      //Declaration and Initialization of variable
		
		System.out.println("Employee name :"+empname);
		System.out.println("Employee id :"+empid);
	}
}
