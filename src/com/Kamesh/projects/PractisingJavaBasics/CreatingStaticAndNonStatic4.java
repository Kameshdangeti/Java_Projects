package com.Kamesh.projects.PractisingJavaBasics;

public class CreatingStaticAndNonStatic4 
{
  public void Statename()
  {
	  System.out.println("AP");
  }
  public void Districs()
  {
	  System.out.println("28");
  }
  public static void capital()
  {
	  System.out.println("Amaravathi");
  }
  public static void Famous()
  {
	  System.out.println("Food,waterfalls,Agricultura,Festivals");
  }
  public static void main(String[] args) 
  {
	CreatingStaticAndNonStatic4 c3=new CreatingStaticAndNonStatic4();
	c3.Statename();
	c3.Districs();
	capital();
	Famous();
  }
}
